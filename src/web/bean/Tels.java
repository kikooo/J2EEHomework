public class Tels {
    private Integer telsid;
    private String telsname;
    private String telnumber;
    private Integer creatorid;

    public Integer getTelsid() {
        return telsid;
    }

    public void setTelsid(Integer telsid) {
        this.telsid = telsid;
    }

    public String getTelsname() {
        return telsname;
    }

    public void setTelsname(String telsname) {
        this.telsname = telsname;
    }

    public String getTelnumber() {
        return telnumber;
    }

    public void setTelnumber(String telnumber) {
        this.telnumber = telnumber;
    }

    public Integer getCreatorid() {
        return creatorid;
    }

    public void setCreatorid(Integer creatorid) {
        this.creatorid = creatorid;
    }
}
